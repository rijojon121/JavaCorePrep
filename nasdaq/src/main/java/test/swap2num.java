package test;

public class swap2num {
  
    public static void main(String[] args) {
        
        int a = 10, b=20;

        System.out.println("Before Swapping the numbers "+ a + " " + b );
        
        int t =a;
        a=b;
        b=t;
        System.out.println("After Swapping the numbers "+ a + " " + b );


    }


}
